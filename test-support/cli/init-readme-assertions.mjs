import {
  assertDoesNotMatchAny,
  assertMatchesAll,
} from '#test-support/assertions/text.mjs';

function assertGeneratedInitReadmeContract(projectReadmeSource) {
  assertMatchesAll(projectReadmeSource, [
    /# openapi-projector Agent Guide/,
    /## 빠른 시작/,
    /## AI agent 작업 지침/,
    /AI coding agent가 매 작업 시작 시 읽는 프로젝트별 작업 지침/,
    /generated `\.api\.ts`를 그대로 실제 앱 코드에 복사하지 않습니다/,
    /사용자가 endpoint를 이미 지정했다면 적용 직전 `prepare`로 최신 Swagger\/OpenAPI를 받아 후보를 갱신한 뒤 그 endpoint를 적용합니다/,
    /마지막 성공 후보 기준으로 진행하고, 사용자에게 오래된 후보일 수 있음을 알립니다/,
    /CLI가 `update`, `upgrade-docs`, 또는 `install-skill --force`를 권장하더라도 자동으로 덮어쓰지 않습니다/,
    /<details>/,
    /<summary>AI Agents: Detailed Workflow<\/summary>/,
    /## For AI Agents: Detailed Workflow/,
    /### Step 2\. 직접 진행하기/,
    /### 기존 작업 공간 업데이트/,
    /npx --yes openapi-projector@latest update/,
    /openapi\/changes\.md/,
    /openapi\/changes\.json/,
    /openapi\/review\/changes\/history\//,
    /openapi\/review\/project-rules\/analysis\.json/,
    /Contract Changed/,
    /npx --yes openapi-projector@latest rules/,
    /rg "fetchAPI\|apiClient\|request\|axios\|ky\|httpClient" src/,
    /openapi\/config\/project-rules\.jsonc/,
    /산출물 경로 필드는 도구가 관리합니다/,
    /Do not change them to force code style or app placement/,
    /npx --yes openapi-projector@latest prepare/,
    /## Swagger 변경 비교/,
    /DTO\/API 후보 생성이 필요하지 않고 Swagger 변경점만 확인할 때는 `refresh`를 단독으로 실행합니다/,
    /## 생성되는 파일/,
    /기본값은 `http:\/\/localhost:8080\/v3\/api-docs`입니다/,
    /대화형 `init`에서 URL 검증이 VPN, 인증, 백엔드 미기동 때문에 실패했다면/,
    /이 문서는 `init` 이후 생성된 작업 안내서입니다/,
    /먼저 볼 파일/,
    /실제 프로젝트 규칙과 맞으면/,
    /프론트엔드 프로젝트 루트는 `package\.json`이 있는 앱 루트입니다/,
    /모든 `openapi-projector` 명령은 프론트엔드 프로젝트 루트에서 실행합니다/,
    /default `sourceUrl` is `http:\/\/localhost:8080\/v3\/api-docs`/i,
    /### 2\. Recommended Prepare Flow/,
    /`prepare`: runs `refresh -> rules -> project`/,
    /Command-by-command fallback/,
    /`prepare`는 아래 흐름을 한 번에 실행합니다/,
    /`refresh`: Swagger\/OpenAPI를 내려받고 이전 버전과 비교해 `openapi\/changes\.md`를 만듭니다/,
    /`rules`: 현재 프론트엔드 프로젝트의 API 호출 규칙과 React Query 사용 여부를 분석해 `openapi\/config\/project-rules\.jsonc`를 만듭니다/,
    /`project`: 검토된 규칙으로 DTO\/API 후보와 선택적 React Query hook 후보를 생성합니다/,
    /React Query 사용 근거로 확인/,
    /처음 실행하면 `rules` 검토 단계에서 멈추는 것이 정상입니다/,
    /`review\.rulesReviewed`를 `true`로 바꿉니다/,
    /`openapi\/review\/project-rules\/analysis\.md`와/,
    /prepare가 rules 검토 단계에서 멈추면/,
  ]);
  assertDoesNotMatchAny(projectReadmeSource, [
    /## 명령 실행 기준/,
    /최신 CLI 기능 강제 사용/,
    /openapi-projector@<version>/,
    /전역 설치 사용/,
    /명령 실행 방식/,
    /## 시작 방법 선택/,
    /## AI agent로 맡기기/,
    /<summary>AI에게 붙여넣을 프롬프트<\/summary>/,
    /### 2\. 선택 설치: oasdiff/,
    /### Codex agent용 프롬프트/,
    /### Codex 스킬 없이 또는 다른 AI agent용 프롬프트/,
    /Option A\. 직접 진행하기/,
    /이 프론트엔드 프로젝트에 openapi-projector Codex 스킬을 사용해줘/,
    /oasdiff/,
    /Compatibility Check/,
    /brew install oasdiff/,
    /### 6\. Git 관리/,
    /<summary>CI\/스크립트에서 프롬프트 없이 실행하기<\/summary>/,
    /npx --yes openapi-projector@latest init --source-url/,
    /openapi-projector Codex 스킬을 사용해줘/,
    /npx --yes openapi-projector@latest install-skill --yes/,
    /사람이 npx --yes openapi-projector@latest prepare를 미리 실행했다면/,
    /prefer the step-by-step flow over `prepare`/i,
    /API wrapper까지 필요하면 위 프롬프트 그대로 쓰면 됩니다/,
  ]);
}

export { assertGeneratedInitReadmeContract };
